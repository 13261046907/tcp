    public static int getQuickCRC16(byte[] buffer, int length) {
        int CRCHi = 0xFF;
        int CRCLo = 0xFF;

        for (int i = 0; i < length; i++) {
            int iIndex = CRCHi ^ (buffer[i] & 0xFF); // Ensure unsigned byte behavior
            CRCHi = CRCLo ^ aucCRCHi[iIndex];
            CRCLo = aucCRCLo[iIndex];
        }

        return (CRCHi << 8) | CRCLo; // Combine high and low bytes into a single short
    }

